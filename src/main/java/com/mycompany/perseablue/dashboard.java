    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String idtanding = idPertandingan.getText();
        String NomorIdentitas = inputNomorIdentitas.getText();
        String tanding = pertandingan.getText();
        String Email = inputEmail.getText();
        try{
            if (NomorIdentitas.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Nomor Identitas tidak boleh ada yang kosong!");
            } else if (Email.isEmpty()){
                JOptionPane.showMessageDialog(null, "Email Tidak Boleh Kosong");
            }else {
                // Menghubungkan java dan sql
                Connection koneksi = (Connection) koneksidb.configDB();

                // Query untuk memeriksa apakah email atau NIK sudah ada
                String sqlCheck = "SELECT COUNT(*) FROM data_pesanan WHERE email = ? AND id_pertandingan = ?";

                // Siapkan pernyataan SQL yang telah dikompilasi sebelumnya untuk eksekusi
                PreparedStatement query_check = koneksi.prepareStatement(sqlCheck);

                // Setel parameter dalam query
                query_check.setString(1, Email);
                query_check.setString(2, idtanding);

                // Eksekusi query dan simpan hasilnya dalam objek ResultSet
                ResultSet result = query_check.executeQuery();
                result.next();  // Pindahkan ke baris pertama hasil

                // If the query result indicates that the email has been used more than 3 times
                if (result.getInt(1) >= 3) {
                    // Tampilkan pesan bahwa email sudah digunakan lebih dari 3 kali
                    JOptionPane.showMessageDialog(null, "Email ini sudah digunakan lebih dari 3 kali untuk pemesanan.");
                } else {
                    Connection PenghubungDatabase = (Connection) koneksidb.configDB();
                    String QueryInsert = "INSERT INTO data_pesanan(email, nomorIdentitas, id_pertandingan, pertandingan) VALUES (?, ?, ?, ?);";
                    PreparedStatement preparedStatement = PenghubungDatabase.prepareStatement(QueryInsert);
                    preparedStatement.setString(1, Email);
                    preparedStatement.setString(2, NomorIdentitas);
                    preparedStatement.setString(3, idtanding);
                    preparedStatement.setString(4, tanding);
                    preparedStatement.executeUpdate();

                    this.setVisible(false);
                    new pemesananTiket().setVisible(true);
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed


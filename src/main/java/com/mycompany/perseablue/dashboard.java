    private void tampil_table(){
        DefaultTableModel tb = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // All cells are not editable
            }
        };
        tb.addColumn("no");
        tb.addColumn("pertandingan");
        tb.addColumn("stadion");
        tb.addColumn("tanggal");
        tb.addColumn("jam");
        
        try {
            int counter =1;
            String query = "SELECT * FROM data_jadwal_pertandingan";
             Connection koneksi = (Connection)koneksidb.configDB();
             PreparedStatement state = koneksi.prepareStatement(query);
             ResultSet Result = state.executeQuery(query);
             while (Result.next()){
                 tb.addRow(new Object[]{
                     Result.getString(1),Result.getString(2), Result.getString(3),Result.getString(4),Result.getString(5)
                 });
             }
            tabelJadwalPertandingan.setModel(tb);
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
        }
        
    }
    
